/**
 * Utility function to get data with fallback
 */
export async function fetchWithFallback<T>(
  fetchFn: () => Promise<T>,
  fallbackData: T,
  errorMessage?: string,
): Promise<T> {
  try {
    const data = await fetchFn();
    // Check if data is empty/null and use fallback
    if (!data || (Array.isArray(data) && data.length === 0)) {
      if (ENABLE_FALLBACK) {
        console.warn(
          errorMessage || "No data received, using fallback data",
          data,
        );
        return fallbackData;
      } else {
        throw new Error(
          errorMessage || "No data received and fallback is disabled",
        );
      }
    }
    return data;
  } catch (error) {
    logger.error(error as Error, {
      context: "fetchWithFallback",
      errorMessage: errorMessage || "Error fetching data",
    });

    if (ENABLE_FALLBACK) {
      console.error(
        errorMessage || "Error fetching data, using fallback:",
        error,
      );
      return fallbackData;
    } else {
      // Re-throw error if fallback is disabled
      throw error;
    }
  }
}

/**
 * Get paginated fallback data
 */
export function getPaginatedFallback<T>(
  data: T[],
  page: number = 1,
  limit: number = 12,
): { data: T[]; hasMore: boolean; nextCursor: string | null } {
  const start = (page - 1) * limit;
  const end = start + limit;
  const paginatedData = data.slice(start, end);

  return {
    data: paginatedData,
    hasMore: end < data.length,
    nextCursor: end < data.length ? `page-${page + 1}` : null,
  };
}
